-- SELECCIONAR TALLA

SELECT QM1_COD, QM1_TALLA_CM, QM1_OEST_TALLA, QM1_NUM_CONSE FROM TQMOVIMIENTOHC
WHERE CONVERT(BIGINT,QM1_COD) = 21610626
--AND QM1_OEST_TALLA = '126'
ORDER BY QM1_NUM_CONSE DESC

-- ACTUALIZAR TALLA

UPDATE TQMOVIMIENTOHC
SET QM1_TALLA_CM = '165.00', QM1_OEST_TALLA = '165.00'
WHERE CONVERT(BIGINT,QM1_COD) = 21610626
--AND QM1_OEST_TALLA = '124'

-- BUSCAR O SELECCIONAR PESO

SELECT QM1_COD, QM1_NUM_PESO, QM1_COD_UNIPESO, QM1_NUM_PESO_A, QM1_NUM_CONSE FROM TQMOVIMIENTOHC
WHERE CONVERT(BIGINT,QM1_COD) = 21610626
--AND QM1_NUM_CONSE = '14'
ORDER BY QM1_NUM_CONSE DESC

